void solve(){
    int n; cin >> n;
    vector<int> v(n);
    for (int i = 0; i < n; i++){
        cin >> v[i];
    }

    int ans = 0;
    for (int i = 0; i < n; i++){
        int target = max(v[n - 1] - v[i], v[i]);
        for (int j = 0; j < i; j++){
            ans += j - (upper_bound(v.begin(), v.begin() + j, target - v[j]) - v.begin());
        }
    }
    cout << ans << '\n';
}

signed main(){
    ios_base::sync_with_stdio(false), cin.tie(NULL);
    int t = 1;
    cin >> t;
    while (t--)
        solve();
    return 0;
}