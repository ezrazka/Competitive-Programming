int nCr_mod2(int n, int r){
    return !(~n & r);
}

void solve(){
    int n; cin >> n;
    vector<int> v(n);
    for (int i = 0; i < n; i++){
        cin >> v[i];
    }

    int ans = 0;
    for (int i = 0; i < n; i++){
        ans ^= v[i] * nCr_mod2(n - 1, i);
    }
    cout << ans << '\n';
}

